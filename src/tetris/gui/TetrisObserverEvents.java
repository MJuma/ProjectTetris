package tetris.gui;

/**
 * Enumeration class holding the possible events
 * an observable can declare.
 * 
 * @author Mohammad Juma
 * @version 11-22-2012
 */
public enum TetrisObserverEvents
{
  /**
   * Repaint event.
   */
  Repaint,
  
  /**
   * Hold Tetromino event.
   */
  HoldTetromino,
  
  /**
   * Lines Cleared event.
   */
  LinesCleared,
  
  /**
   * New Sequence event.
   */
  NewSequence,
  
  /**
   * Tells the observer the game has ended.
   */
  GameOver;
}
